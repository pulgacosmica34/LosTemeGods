                Taekwondo es un término que procede del coreano y que hace referencia a un
                arte marcial del mismo origen. Este deporte combina técnicas del Kung fu, el
                karate-do y otras disciplinas más antiguas. La expresión se forma por tres
                términos: Tae (que refiere a la utilización de los pies), Kwon (vinculado a los
                brazos y puños) y Do (una noción filosófica que se asocia al camino hacia la
                perfección). La frase "Tae Kwon Do", por lo tanto, alude al uso de los pies y las
                manos en una técnica que, por extensión, no apela a ninguna clase de
                armamento. <br>
                Taekwondo por lo tanto significa "el camino de los pies y las manos".
