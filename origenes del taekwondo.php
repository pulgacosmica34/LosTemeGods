            <p class="encabezado">
                El hombre tiene por naturaleza el instinto de conservar tanto su propia vida como
                la de su raza, y por tanto desarrolla actividades físicas bien sea consciente o
                inconscientemente. El hombre no puede prescindir de los movimientos físicos,
                crece y evoluciona en ellos, sin importar el tiempo y el espacio. En la antigüedad
                las personas no tenían otros medios más que sus propias manos desnudas y sus
                cuerpos para defenderse, así que naturalmente desarrollaron técnicas de combate
                mano a mano. Incluso en la época en que se desarrollaron las armas como
                medios defensivos u ofensivos, las personas aún seguían disfrutando de las
                técnicas de combate mano a mano con el propósito de fortalecer sus cuerpos, así
                como de lucirse en los combates durante los rituales de las comunidades tribales.
                <br>
                En los comienzos de la península coreana había tres tribus, cada una contaba con
                una competencia de arte marcial para guerreros durante la época de rituales. Para
                ese entonces, la gente aprendía sus técnicas de las experiencias de pelear con
                animales, cuyos movimientos defensivos y ofensivos también eran objeto de
                análisis. Se cree que éste es exactamente el verdadero origen del taekwondo
                moderno, cuyo nombre proviene de "Subak", "Taekkyon", entre otros.
